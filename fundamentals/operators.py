# operators
# arithametic operators(addition,subtraction,multiplication,divison)

num1=100
num2=200

addition_result=num1 + num2
print(f"{num1} + {num2} = {addition_result}")

subtraction_result=num1 - num2
print(f"{num1} - {num2} = {subtraction_result}")

multiplication_result=num1*num2
print(f"{num1} * {num2} = {multiplication_result}")

division_result=num1/num2
print(f"{num1} / {num2} = {division_result}")

modulus_result=num1%num2
print(f"{num1} % {num2} = {modulus_result}")

exponent_result=num1**2
print(f"{num1} **{2} = {exponent_result}")

floordivision_result=num1//num2
print(f"{num1} // {num2} = {floordivision_result}")